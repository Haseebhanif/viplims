    public function transactions($id)
    {
        try {
            $package = UserPackage::find($id);
            $response = json_decode($package->response);
            $paypal = new PaypalController();
            $data['subscription_id'] = $response->id;
            $data['transactions'] = $paypal->get_transactions($response->id,$response->start_time,Carbon::now()->toISOString());

            return view('transactions.index',$data);
        }catch (\Exception $e)
        {
            flash('Something goes wrong'.$e);
            return redirect()->back();
        }
    }

